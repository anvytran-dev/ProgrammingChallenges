//P: The parameter is the variable that will take the boolean value.
//R: Return either the "Yes" or "No" string based on the condition. 
//E: If the boolean value is true, return "yes."
//P: If bool value is true, return "yes." If bool value is false, return "no."
function boolToWord(bool){
  
  if (bool != true){
       return "No"   
  } else if (bool = true){
    return "Yes"
  }
}
